public class Dados {
    public static void main(String[] args) {
        // Numeros pequenos/medianos(tamanho):
        byte nota1 = 18;
        short nota2 = 2355;
        // Numeros longos:
        // int num1 = 1000000;
        // long num2 = 100_000_000_000L;
        int somaInt = (nota1 + nota2);

        System.out.println(somaInt);

        //Float - double:
        float float1 = 1.5F;
        float float2 = 1.5F;
        float somaDec = (float1 + float2);

        System.out.println(somaDec);
    }
}
